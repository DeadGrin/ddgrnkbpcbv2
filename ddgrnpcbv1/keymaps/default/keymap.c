static void keypress_show_position_on_oled(uint8_t x, uint8_t y, bool W_or_B, uint8_t sideLength)
{
    uint8_t actualXpos = x*(sideLength+1);
    uint8_t actualYpos = y*(sideLength+1);

    for(uint8_t i = 0; i < sideLength; i++)
    {
        for(uint8_t j = 0; j < sideLength; j++ )
        {
            oled_write_pixel(actualXpos+i, actualYpos+j, W_or_B);
        }
    }  
}

                         
